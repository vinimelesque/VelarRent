    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        String usuario = campoUser.getText();
        String senha = campoSenha.getText();

        if (usuario.equals("ds") && senha.equals("ds")) {
            JOptionPane.showMessageDialog(null, "Login e senha v√°lidos.");
            JFrame Home = new Home();
            Home.setVisible(true); //janela cadastro visivel
            Home.setLocationRelativeTo(null); //centralizar tela

            JFrame Login = new Login();
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Login e senha inv√°lidos.");
            campoUser.setText(""); //limpa o campo usu√°rio
            campoSenha.setText(""); //limpa o campo senha
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

